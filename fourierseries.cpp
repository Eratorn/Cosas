sf::Vector2f complexMult(sf::Vector2f z1, sf::Vector2f z2)
{

    sf::Vector2f z = { 0, 0 };
    z.x += z1.x * z2.x - z1.y * z2.y;
    z.y += z1.x * z2.y + z1.y * z2.x;
    return z;
}
sf::Vector2f coef(int npoints, int n, std::vector<sf::Vector2f>& f)
{
    float dt = 1.0f / npoints;
    sf::Vector2f result = { 0, 0 };
    float t = 0;
    for (int i = 0; i < npoints; i++)
    {
        float arg = -2 * M_PI * n * t;
        sf::Vector2f z = complexMult(f[i], { cos(arg), sin(arg) });
        z.x *= dt;
        z.y *= dt;
        result.x += z.x;
        result.y += z.y;
        t += dt;
    }
    return result;
}
class Arrow
{
public:
    sf::Vector2f update(float t, int n, std::vector<sf::Vector2f>& points, float x, float y)
    {
        auto c = coef(N, n, points);
        float arg = 2 * M_PI * n * t;
        sf::Vector2f z = complexMult(c, { cos(arg), sin(arg) });
        float ang = atan2(z.y, z.x);
        float mag = sqrt(z.x * z.x + z.y * z.y);
        r = mag;
        line[0].position = { x, y };
        line[1].position = { x + mag * cos(ang), y + mag * sin(ang) };
        line[1].color = sf::Color::Blue;
        line[0].color = line[1].color;
        return line[1].position;

    }
    void draw(sf::RenderTarget& rt)
    {
        
        sf::CircleShape c(r);
        c.setOrigin({ r, r });
        c.setPosition(line[0].position);
        c.setOutlineColor(sf::Color(255, 255, 255, 50));
        c.setOutlineThickness(1.0f);
        c.setFillColor(sf::Color::Transparent);
        rt.draw(line, 2, sf::Lines);
        if (::draw) rt.draw(c);
       
    }
private:
    sf::Vertex line[2];
    float r = 0;
};
